<h1>Listrik Pra Bayar</h1>
<hr />
<p>Inilah inovasi terkini dari layanan PLN yang lebih menjanjikan Kemudahan, Kebebasan dan Kenyamanan bagi pelanggannya : Listrik Prabayar Listrik Isi Ulang ! </p>

<p> Dengan listrik prabayar, setiap pelanggan bisa mengendalikan sendiri penggunaan listriknya sesuai kebutuhan dan kemampuannya. Seperti halnya pulsa isi ulang pada telepon seluler, maka pada sistem listrik prabayar, pelanggan juga terlebih dahulu membeli token (voucher listrik isi ulang) yang terdiri dari 20 digit nomor yang bisa diperoleh melalui gerai ATM sejumlah bank atau melalui loket-loket pembayaran tagihan listrik online. </p>

<p> Lalu, 20 digit nomor token tadi dimasukkan (diinput) ke dalam kWh Meter khusus yang disebut dengan Meter Prabayar (MPB) dengan bantuan keypad yang sudah tersedia di MPB. </p>

<p> Nantinya, lewat layar yang ada di MPB akan tersajikan sejumlah informasi penting yang langsung bisa diketahui dan dibaca oleh pelanggan terkait dengan penggunaan listriknya, seperti : </p>

<ul>
	<li> Informasi jumlah energi listrik (kWH) yang dimasukkan (diinput).</li><br />
    <li> Jumlah energi listrik (kWH)) yang sudah terpakai selama ini </li><br />
    <li> Jumlah energi listrik yang sedang terpakai saat ini (real time).</li><br />
    <li> Jumlah energi listrik yang masih tersisa.</li><br />
</ul>

<p> Jika energi listrik yang tersimpan di MPB sudah hampir habis, maka MPB akan memberikan sinyal awal agar segera dilakukan pengisian ulang. Dengan demikian, pelanggan secara real time, setiap saat, kapan saja dapat mengetahui secara persis penggunaan listrik di rumah. Jadi, kendali penggunaan listrik sungguh ditangan anda !.</p>
